      <h6>Blizzard Entertainment, Vicarious Visions</h6>
      <img :src="'./images/gomy2021/diablo-2-resurrected.gif'" />
      <p>
        I played this once through with my brother, and can say that the team
        that Blizzard gave this to for the remastering treatment did great with
        it. I could see going back to this again with others or to play through
        on the harder difficulties. It's fittingly so that the best thing
        Blizzard has put out in quite a while is a remake of their pièce de
        résistance from 20 years ago.
