      <h2>
        <mark>Games of My Year</mark>
      </h2>
      <p>
        Generally speaking, my criteria for GOTY is pretty loose, although one
        common theme that I personally have noticed is: if by the end of a given
        experience I am thinking "I can't wait to play this again someday" then
        there's a good chance it belongs in this list. This is particularly true
        because I personally very rarely replay games. I also try not to limit
        this list to any specific length (because, well, some years you just
        play a lot of great games), while also trying to resist the temptation
        to just put 15+ games here.
      </p>
      <h4 class="bold">Disco Elysium</h4>
      <h6>Robert Kurvitz</h6>
      <img :src="'./images/gomy2021/disco-elysium-1.gif'" />
      <p>
        There is way more to say about this game than I could ever conjure into
        words. It really feels like it speaks for itself entirely. Every moment
        is packed with emotion and dripping with style, and it is so consistent
        with itself that it feels almost like it was birthed whole-cloth as a
        single entity.
      </p>
      <p>
        The city the game takes place in, and the exposed lore of the world that
        this city lives amongst, and all of its people and their place in that
        world all feel so real. Every "quest" you come across never feels as
        such, but just another person in this world living their life and
        integrating you into it in the ways that they feel like doing. It's also
        worth noting that I generally dislike the "amnesia" main protagonist
        trope, but this game builds its entire premise and plot on it which
        makes it feel believable and worthy. It also can't be overstated how
        impactful the soundtrack is in
        <i>Disco Elysium</i>. It is another character in the story and the
        world, it ties everything together, and it makes you feel everything
        Harry feels.
      </p>
      <img :src="'./images/gomy2021/disco-elysium-2.gif'" />
      <p>
        In my opinion, this might be one of the best games ever crafted, and it
        will no doubt go down in the annals of history as a seminal classic of
        interactive fiction and creativity. I can't wait to play it again some
        day and explore the hundreds of decisions and options I didn't stumble
        across the first time around.
      </p>
      <h4 class="bold">Returnal</h4>
      <h6>Housemarque</h6>
      <img :src="'./images/gomy2021/returnal-1.gif'" />
      <p>
        I don't think I've ever been as deeply immersed in a game as I was with
        <i>Returnal</i>. From a flow-state gameplay perspective absolutely, but
        also as a can't-stop-thinking-about-it brain worm life feeling
        (coincidentally though, <i>Disco Elysium</i> may be the only other game
        I've ever played that gave me that same feeling). I finished the
        experience across 6 intensely condensed days of playing, and there was
        once that I think I launched and played the game 4 separate times in a
        single day (admittedly for shorter sessions each, but its worth noting
        how unlike me it is to do this). The world immersion and the extreme
        precision and care put into the game design flow itself mostly
        contributed to my not being able to stop thinking about it.
      </p>
      <img :src="'./images/gomy2021/returnal-2.gif'" />
      <p>
        After such wonderful gameplay systems and world design, everything else
        about the game feels like icing on the cake. The particle effect design,
        the ear-candy sound design, the creature models, the
        random-but-not-too-random level generation, and the
        punishing-but-forgiving-enough play loop to name a few. Then it's all
        capped off beautifully by its what-the-hell-is-going-on narrative and
        our main protagonist's great voice work. I didn't get the true "Act 3"
        ending, but I'll likely revisit the game in a few years and see that
        conclusion through.
      </p>
      <h4 class="bold">Mutazione</h4>
      <h6>Die Gute Fabrik</h6>
      <img :src="'./images/gomy2021/mutazione.gif'" />
      <p>
        This game embodies so many of the reasons I play games. Games that allow
        us to escape to different worlds, with their own problems but also their
        own beauties that our own world can't have. Games that let us become
        somebody we aren't (in this case, a teenage girl going on a
        coming-of-age summer vacation trip to an irradiated fallout island full
        of quirky mutated humans...). Games that give us feelings and
        connections we can't otherwise find in our day-to-day lives. Games that
        create a space for us to laugh or cry or think or just be and feel.
        Games that manifest meaning from nothing, carefully weaving together an
        intricate pattern of sound and visual and text with interaction.
        Mutazione accomplishes all of this and so much more, and all with some
        beautifully simple gameplay mechanics.
      </p>
      <h4 class="bold">"DEATHLOOP"</h4>
      <h6>Arkane Studios</h6>
      <img :src="'./images/gomy2021/deathloop.gif'" />
      <p>
        I expected this game to be much more like "<i>Dishonored</i>
        but with a time-loop" than it ends up being in reality. To be fair, it
        shares so many elements with
        <i>Dishonored</i> , so I can see why I might have thought that. But, the
        entire structure, tone, and even much of the moment-to-moment gameplay,
        is quite different. The DNA though is clearly there, and the level
        design is unquestionably descendent. And let's be honest, the level
        design is the shining star of the <i>Dishonored</i> franchise anyway.
      </p>
      <p>
        It's well established online why everybody thinks so highly of this
        game, and it's all spot on. The world design is distinctive, and the
        cast of characters are quite memorable despite the player interacting
        directly with some of them very infrequently. It all feels so well
        realized and consistent with its bizarre and captivating sci-fi premise.
        My experience overall felt complete, so I may never actually revisit
        this game's campaign, but I loved my time with it and I could see them
        doing some DLC for it which would be a welcome addition.
      </p>
      <h4 class="bold">Manifold Garden</h4>
      <h6>William Chyr Studio</h6>
      <img :src="'./images/gomy2021/manifold-garden.gif'" />
      <p>
        The mind-bending visuals of this game are enough to set it apart from
        many other games, but the puzzle design and bizarre avant-garde approach
        to story telling elevate it further. It's the rare case where I actually
        feel as though I'm playing as myself in this game, as it makes no effort
        to give the game's avatar any story, context, or personality. It's a
        direct connection between me and the game, facilitated through an
        interactive first-person camera control.
      </p>
      <p>
        Experiencing <i>Manifold Garden</i> gave me the same feeling that you
        might get from an indie film, one that you feel is so quirky or specific
        or meaningful that it could have only been created specifically for you.
        The game expresses itself in such an experimental way, and entirely
        succeeds at making you think about things you never expected to think
        about from a game with no text or explicit narrative.
      </p>
