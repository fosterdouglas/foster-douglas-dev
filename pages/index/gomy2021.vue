        <mark>Honorable Mentions</mark>
      </h2>
      <h4 class="bold">Control</h4>
      <h6>Remedy Entertainment</h6>
      <img :src="'./images/gomy2021/control.gif'" />
      <p>
        Jesse is such a well-written character and the world of Control is such
        an interesting blend of so many sci-fi tropes and still emerges as
        something completely distinct. Even a year after playing it, I still
        remember the names of the characters and a lot of the specifics of what
        happens in the story. It also feels like a really meaningful evolution
        of the "documents" and "audio logs" trope that a lot of these games fall
        into; it never feels like those items scattered about are "lore dump"
        but are a very meaningful way of world building. I can't wait to see
        what Remedy does next, although I don't imagine I'll be playing the
        suspense-turned-horror Alan Wake 2, unfortunately.
      </p>
