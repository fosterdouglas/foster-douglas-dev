      <h2>
        <mark>Most Surprising</mark>
      </h2>
      <h4 class="bold">Tearaway Unfolded</h4>
      <h6>Media Molecule</h6>
      <img :src="'./images/gomy2021/tearaway-unfolded.gif'" />
      <p>
        This has all the charm of a Media Molecule
        <i>Littlebigplanet</i> entry, but as a third-person adventure story. I'm
        not sure why I was so surprised by this, but the game had an incredible
        amount of creativity in how it used the PS4's controller, and how it
        incorporates the "You" (the player) into the game in interesting meta
        ways. Although the game was about twice as long as it needed to be for
        my taste, all of it still feels fresh and innovative throughout.
      </p>
      <h4 class="bold">Dragon Quest XI S: Echoes of an Elusive Age</h4>
      <h6>Square Enix</h6>
      <img :src="'./images/gomy2021/dq-11s.gif'" />
      <p>
        Having never played a <i>DQ</i> game and sort of insinuating what they'd
        be like based on some random impressions, this game was a massive
        surprise to me (although it may be the case that it is the least
        <i>Dragon Quest</i>-like entry in the series). The production value, and
        in particular the music, is astonishing. It tries a lot of unique things
        and succeeds with most of them, and it really feels like a nice
        evolution or hybridization of a classic JRPG.
      </p>
      <hr />
      <h2>
        <mark>Most Disappointing</mark>
      </h2>
      <h4 class="bold">The Pathless</h4>
      <h6>Giant Squid</h6>
      <img :src="'./images/gomy2021/the-pathless.gif'" />
      <p>
        I know that this game is beloved by a lot of fans, and I can definitely
        see why, but to me the game design feels ham-fisted and disjointed. The
        open world is beautiful and moving through it fluidly and "flow"-like
        feels really special. The decision to then make it into an Ubisoft-style
        "activate the towers" game design is a bit disappointing, but not what
        soiled the experience for me. It's the boss design that feels
        unnecessary (although I've seen plenty of discourse heavily praising
        it). It doesn't feel like it fits with the vibe of the world, almost as
        if an executive came in at the end of development and said "this is
        great, but you know what it's missing? BOSSES." So much else about the
        game is experimental that I feel they could have leaned even harder into
        it to better tie it all together for me.
      </p>
      <h4 class="bold">Sayonara Wild Hearts</h4>
      <h6>Simogo</h6>
      <img :src="'./images/gomy2021/sayonara-wild-hearts.gif'" />
      <p>
        Outside of all of the unfortunate controversy around the development
        team on this game, which sullies its appeal as it is, I came away after
        playing it with a weird mix of inspired and frustrated. My primary issue
        with the gameplay is that it doesn't encourage experimentation. The game
        has a notable and stellar soundtrack, and the game feels at its best
        when the player can get into a flow with the music and traverse the
        levels. But, with such strict level design in some cases, you'll find
        yourself replaying sections of levels over and over to get past them.
        While there's nothing wrong with that in theory, being restarted at
        checkpoints feels deeply at odds with the flow of the gameplay style.
      </p>
      <h4 class="bold">Maquette</h4>
      <h6>Graceful Decay</h6>
      <img :src="'./images/gomy2021/maquette.gif'" />
      <p>
        It's easy to sum this up, which is to say a very cool idea executed a
        bit poorly. It's more focused on telling a (heartfelt, for sure)
        sob-story about a breakup someone had than it is concerned with the
        puzzle-designed experience of the player, unfortunately. If this type of
        game is interesting to you, check out
        <i>Superliminal</i>, <i> Manifold Garden</i>, or <i>The Pedestrian</i>,
        just a few of many great games in the puzzle genre that I played in
        2021.
      </p>
      <h4 class="bold">Raji: An Ancient Epic</h4>
      <h6>Nodding Head Games</h6>
      <img :src="'./images/gomy2021/raji-an-ancient-epic.gif'" />
      <p>
        Unfortunately, this experience didn't quite come together for me. The
        controls and combat design are incredibly frustrating, to the extreme
        that it felt borderline unplayable at times personally. The visuals are
        great, and the story-telling style and setting are quite unique and
        distinct, but neither is enough to carry the entire experience.
      </p>
      <h4 class="bold">Outer Wilds: Echoes of the Eye</h4>
      <h6>Mobius Digital, Annapurna Interactive</h6>
      <img :src="'./images/gomy2021/outer-wilds-echoes-of-the-eye.gif'" />
      <p>
        This doesn't technically belong in this section, but I had such a
        polarizing experience with Outer Wilds' DLC that it's worth pointing
        out. The first hour or two of this 8+-hour DLC was mind-blowing and
        somehow manages to up the ante on what was one of the best games I've
        ever played as is. Unfortunately (for me), it soon devolves into a
        (admittedly well-designed) horror-survival experience, in ways that the
        original game never even came close to being. It was soon obvious to me
        that there was no way I was going to enjoy getting through the
        experience, so I watched a playthrough of it instead. There is so much
        positive to say about this expansion, and there are three distinct "oh
        my gosh" revelations which each one-up the other in ways that are so
        incredible and inspiring and unforgettable. There is absolutely a
        reality where this game could have had a mode without the aspect that
        made it survival-horror, but I do believe I'm the minority in this
        opinion as most online discourse seems to enjoy the specific mechanic I
        felt frustrated by.
      </p>
