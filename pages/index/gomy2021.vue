      <h2>
        <mark>Notable Indies</mark>
      </h2>
      <h4 class="bold">Ikenfell</h4>
      <h6>Happy Ray Games</h6>
      <img :src="'./images/gomy2021/ikenfell.gif'" />
      <p>
        I played this early in the year, but I remember being wholly charmed by
        it. It has a unique turn-based combat system that feels like a
        simplified variation on <i>Paper Mario</i> and <i>Tactics</i> games, and
        the characters and narrative were memorable and interesting. If I were a
        kid now, I could even see myself enjoying it as much as I enjoyed
        <i>Chrono Trigger</i> as a kid (although of course
        <i>Chrono Trigger</i> is more seminal in every way, that distinction
        might be subtle to younger audiences).
      </p>
      <h4 class="bold">Chicory: A Colorful Tale</h4>
      <h6>Greg Lobanov, Finji</h6>
      <img :src="'./images/gomy2021/chicory-a-colorful-tale.gif'" />
      <p>
        This game wears its 2D <i>Zelda</i> inspirations proudly on its sleeve,
        and in certain aspects it actually succeeds in ways that the 2D
        <i>Zelda</i> games don't entirely. Which is to say with its storytelling
        and emotional heart. It has much less "combat" (but by no means
        completely absent of conflict and tension), and I love the focus on
        character interaction and how unique the painting mechanic is. It's not
        just another hero carrying a sword and shield, but a vulnerable and
        grounded protagonist who grows with the player. Weirdly similar to my
        comment on <i>Ikenfell</i>, I could absolutely see this being an
        inspirational game for someone younger, and that in the future they
        would look back fondly on playing it as a child. If the Games of the
        Year section wasn't so packed already, this game would have probably
        made it there.
      </p>
      <h4 class="bold">TOEM</h4>
      <h6>Something We Made</h6>
      <img :src="'./images/gomy2021/toem.gif'" />
      <p>
        A game all about taking photos. It's so charming, so easy to play and
        "cozy" feeling, has a great mix of limited exploration and satisfying
        collectibles, and the perfect overall length. Pulling off black and
        white seems so tricky normally but it pulls it off with distinction and
        is the perfect choice for the game, both visually and conceptually for a
        game based around photography to focus on composition and reference the
        roots of the medium.
      </p>
      <h4 class="bold">Alba: A Wildlife Adventure</h4>
      <h6>Ustwo, Plug In Digital</h6>
      <img :src="'./images/gomy2021/alba-a-wildlife-adventure.gif'" />
      <p>
        Another... game all about taking photos. This game hit me in some
        interesting ways, and I was surprised at how it had a purpose and
        "mission" beyond the game itself (broadly, it's about wildlife
        preservation), and how it gently preaches that mission to the player.
        It's also really interesting to play directly as a child, and I don't
        think most games that try this really pull it off but here its done with
        a lot of whimsy and care.
      </p>
      <h4 class="bold">Everything</h4>
      <h6>David OReilly</h6>
      <img :src="'./images/gomy2021/everything.gif'" />
      <p>
        In some ways, this sometimes barely feels like a "game," and in other
        ways I consider it as a contender for best of the year. It's a great
        example of effective organic exploration design. It's also in the rare
        category of explicitly incorporating philosophy in its narrative, and
        the even rarer category of that philosophy being delivered directly by
        Alan Watts. As with all the games in the Games of the Year group, even
        though this one isn't, I could absolutely see replaying this some day in
        the future, if for no other reason than an excuse to listen to a couple
        of hours of Alan Watts philosophize over top a
        <i>Katamari</i>-infused universe simulator.
      </p>
      <h4 class="bold">Void Bastards</h4>
      <h6>Blue Manchu</h6>
      <img :src="'./images/gomy2021/void-bastards.gif'" />
      <p>
        It's hard to pinpoint exactly why I enjoyed this game so much. I do know
        that all the systems feel exactly as complex as they need to be, and
        that the gameplay loop is challenging but fair and varied enough to stay
        interesting for the 10 hours or so it takes to finish a single run. The
        humor and art style are also spot on.
      </p>
      <hr />
      <h2>
        <mark>Notable Strategy</mark>
      </h2>
      <h4 class="bold">Desperadoes III</h4>
      <h6>Mimimi Games</h6>
      <img :src="'./images/gomy2021/desperadoes-3.gif'" />
      <p>
        The strategy game design was both satisfyingly open-ended, allowing
        dozens of solutions to any given area, while also still retaining
        direction and momentum forward. I found myself often surprised at how
        creative I could be, and sometimes needed to be, to get through certain
        sections and accomplish some of the optional goals too. Also
        surprisingly, the narrative was quite interesting, though not
        ground-breaking, and felt like a step forward from their previous title,
        <i>Shadow Tactics</i>.
      </p>
      <h4 class="bold">Into the Breach</h4>
      <h6>Subset Games</h6>
      <img :src="'./images/gomy2021/into-the-breach.gif'" />
      <p>
        From the people that did <i>FTL</i>, this is a thoughtful experience
        with meticulously designed turn-based strategy and endless variation. I
        only played through the game once, despite the game heavily encouraging
        multiple playthroughs. That's actually my only complaint with the game,
        that it could have maybe strung together a few sets of the 4 islands to
        create something more like a standard campaign to play through, instead
        of individual disconnected runs. Regardless, one of the best modern
        strategy games I've maybe ever played.
      </p>
      <h4 class="bold">Frostpunk</h4>
      <h6>11 Bit Studios</h6>
      <img :src="'./images/gomy2021/frostpunk.gif'" />
      <p>
        A complex and tense game with a pretty narrow design for viable
        strategies, but still a lot fun regardless. In particular, the "prequel"
        DLC that was released was my favorite scenario of the lot because it
        deviated the most from the rest of the game's stricter style. I'm glad
        the game was successful enough to warrant a sequel eventually, which was
        recently announced. I'm also very excited for the board game adaptation
        that's coming.
      </p>
      <h4 class="bold">Slay the Spire</h4>
      <h6>Mega Crit Games</h6>
      <img :src="'./images/gomy2021/slay-the-spire.gif'" />
      <p>
        In the category of "rougelike" games with loads of relics to collect to
        then use to break the rules, this game lives next to the greatest games
        of this style like <i>Dead Cells</i> , <i>The Binding of Isaac</i> , and
        <i>Risk of Rain</i>, except... it's a card-based strategy game. It's
        since paved the way for an entire genre of card-action games (which are
        distinct from what <i>Hearthstone</i> does, I think). I played it much
        less than I was tempted to, but I did play through to the end boss with
        the three primary classes and thoroughly enjoyed its deep mechanics.
      </p>
      <hr />
      <h2>
        <mark>Most Addictive</mark>
      </h2>
      <h4 class="bold">Planet Coaster</h4>
      <h6>Fontier Developments</h6>
      <img :src="'./images/gomy2021/planet-coaster.gif'" />
      <p>
        This was my Pandemic comfort game, for sure. I put 100 hours into it
        (generally rare for me for a single game), and really enjoyed creating
        coasters and micromanaging the aspects of the parks and employees. It
        feels like an optimized balance loop between strategy and creativity,
        creating something unique in your park and seeing how that affects the
        ecosystem and then adapting and doing that process over and over again.
        It feels like a worthy successor to the
        <i>Rollercoaster Tycoon</i> series, and is actually a better experience
        in most ways.
      </p>
      <h4 class="bold">Two Point Hospital</h4>
      <h6>Two Point Studios</h6>
      <img :src="'./images/gomy2021/two-point-hospital.gif'" />
      <p>
        Similarly, this game is clearly inspired by Bullfrog's classic
        <i>Theme Hospital</i> from the 90s, a goofy-in-tone but serious-strategy
        hospital management sim. This game takes that goofy tone even further,
        which works really well. My only complaint with the experience is that
        once you realize how important the room decorations that level up each
        room in the hospital are, it feels like you can get through things a lot
        easier in ways that maybe didn't feel intended in the design. I'm
        definitely looking forward to the team's university-themed follow-up
        this year, <i>Two Point Campus</i>.
      </p>
